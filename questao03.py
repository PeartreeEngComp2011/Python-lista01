result_b = (3 * num1) + num3
result_c = num3 ** 3

print("O resultado do produto do dobro do primeiro com metade do segundo é:", result_a)
print("O resultado da soma do triplo do primeiro com o terceiro é:", result_b)
print("O resultado do terceiro elevado ao cubo é:", result_c)
